	private float total;
	private float liquido;
	private float proventos;
	private float eventuais; 
	private float judiciais;
	private float tetoRedutor;
	private float irrf;
	private float contribuicao;
	private float previdencia;

	public String getVinculo() {
		return vinculo;
	}

